#include<stdio.h>
int main ()
 {
   int n , cube=0;
   printf("Enter any number : ");
   scanf("%d",&n);
   cube = n*n*n; 
   printf("Cube of %d is : %d ",n,cube);
   return 0 ; 
 
 }
 
 
